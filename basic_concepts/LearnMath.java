public class LearnMath {
	public static void main(String[] args) {
		int mynum1 = 1000;
		int mynum2 = 200;

		// how to use Math library

		// find max in two value
		System.out.println(Math.max(mynum1, mynum2));
		// find min in two value
		System.out.println(Math.min(mynum1, mynum2));
		// root number
		System.out.println(Math.sqrt(70));
		// absoulte number
		System.out.println(Math.abs(-7.2));
		// create random
		System.out.println(Math.random());

		// and so on look on documentation
	}
}